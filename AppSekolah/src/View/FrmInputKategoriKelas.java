    private void isicomboIDKelas(){
        ListIDKelas = daokategorikelas.SelectKelas();
        CmbKelas.removeAllItems();
        for (KategoriKelas data : ListIDKelas){
            CmbKelas.addItem(data.getId_kelas());
        }
    }
    
    
    private void refreshIsiTable(){
        isicomboIDKelas();
        if(proses.equalsIgnoreCase("")){
            ListKategoriKelas = daokategorikelas.SelectKategoriKelas();
        }
        ListKategoriKelas = daokategorikelas.SelectKategoriKelas();
        dtm = (DefaultTableModel) TblKategoriKelas.getModel();
        dtm.setRowCount(0);
        for (KategoriKelas data : ListKategoriKelas){
            dtm.addRow(new Object[]{
                data.getId_kategori(),
                data.getDaftar_kelas(),
                data.getId_kelas()});
        }
        if (TblKategoriKelas.getRowCount()>0){
            baris = TblKategoriKelas.getRowCount()-1;
            TblKategoriKelas.setRowSelectionInterval(baris, baris);
            aturButton(true);
            aturText(false);
            proses="";
        }
    }

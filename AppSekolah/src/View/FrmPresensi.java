    private void BtnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInputActionPerformed
        // TODO add your handling code here:
        presensi = new Presensi();
        presensi.setId_presensi(TxtIdPresensi.getText());
        presensi.setNis(CmbNamaSiswa.getSelectedItem().toString());
       String tgl = new SimpleDateFormat("dd-MM-yyyy").format(CldTanggal.getDate());
       presensi.setTgl_presensi(tgl);
        presensi.setStatus(CmbStatus.getSelectedItem().toString());
        presensi.setId_kategori(CmbKategoriKelas.getSelectedItem().toString());
        presensi.setId_kelas(CmbKelas.getSelectedItem().toString());
        presensi.setKet(TxtKeterangan.getText());
        
        if(proses.equalsIgnoreCase("INSERT")){
            status=daoPresensi.insertPresensi(presensi);
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil",
                "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else{
            status=daoPresensi.updatePresensi(presensi);
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah",
                "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        if(status==false){
               JOptionPane.showMessageDialog(null, "Penyimpanan Data Gagal",
                "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        refreshIsiTable();
    }//GEN-LAST:event_BtnInputActionPerformed

    private void BtnGagalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGagalActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null, "Apakan Yakin Ingin Menghapus Data Ini?",
                "Konfirmasi",JOptionPane.YES_NO_OPTION)
                ==JOptionPane.YES_OPTION){
            status=daoPresensi.deletePresensi(TxtIdPresensi.getText());
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
            if (status==false){
                    JOptionPane.showMessageDialog(null, "Hapus Data Gagal",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);  
            }
refreshIsiTable();
        }
    }//GEN-LAST:event_BtnGagalActionPerformed


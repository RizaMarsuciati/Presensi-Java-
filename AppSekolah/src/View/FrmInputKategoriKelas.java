    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        // TODO add your handling code here:
        kategorikelas = new KategoriKelas();
        kategorikelas.setId_kategori(TxtIdKategoriKelas.getText());
        kategorikelas.setDaftar_kelas(TxtKategoriKelas.getText());
        kategorikelas.setId_kelas(CmbKelas.getSelectedItem().toString());
        
        if(proses.equalsIgnoreCase("INSERT")){
            status = daokategorikelas.insertKategoriKelas(kategorikelas);
            
            JOptionPane.showMessageDialog(null,"Penyimpanan Data Berhasil",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else{
            status = daokategorikelas.updateKategoriKelas(kategorikelas);
     
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Update",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        if(status==false){
            
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Gagal",
                "Informasi", JOptionPane.INFORMATION_MESSAGE);
             refreshIsiTable();
        }
        
       
    }//GEN-LAST:event_BtnSimpanActionPerformed


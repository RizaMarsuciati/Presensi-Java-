private void isicombo(){
    listIDkelas = daoPresensi.SelectKelas();
    CmbKelas.removeAllItems();
    for (Presensi data : listIDkelas){
        CmbKelas.addItem(data.getId_kelas());
    }
     listIDkategorikelas = daoPresensi.SelectKategoriKelas();
    CmbKategoriKelas.removeAllItems();
    for (Presensi data : listIDkategorikelas){
        CmbKategoriKelas.addItem(data.getId_kategori());
    }
       listIDsiswa = daoPresensi.SelectSiswa();
    CmbNamaSiswa.removeAllItems();
    for (Presensi data : listIDsiswa){
       CmbNamaSiswa.addItem(data.getNis());  
    }
}

private void refreshIsiTable() {
    isicombo();
    if (proses.equalsIgnoreCase("")){
        listPresensi = daoPresensi.SelectPresensi("", "");
        
    }else if (proses.equalsIgnoreCase("CARI")){
            listPresensi = daoPresensi.SelectPresensi(TxtCari.getText(),TxtCari.getText());
    }
   dtm = (DefaultTableModel) TblPresensi.getModel();
   dtm.setRowCount(0);
   for (Presensi data : listPresensi){
       dtm.addRow(new Object[]{
           data.getId_presensi(),
           data.getNis(),
           data.getTgl_presensi(),
           data.getStatus(),
           data.getId_kategori(),
           data.getId_kelas(),
           data.getKet()});
   }
   if (TblPresensi.getRowCount() > 0){
       baris=TblPresensi.getRowCount() - 1;
       TblPresensi.setRowSelectionInterval(baris, baris);
       aturButton(true);
       aturText(false);
       proses="";
   }
}
